float normal[] = { 0,0,0 };

void vector_colculate(std::vector <float> mas, std::vector <float> mas2)
{
	normal[0] = (mas[1] * mas2[2] - mas[2] * mas2[1]);
	normal[1] = (mas[0] * mas2[2] - mas2[0] * mas[2]) * (-1);
	normal[2] = (mas[0] * mas2[1] - mas[1] * mas2[0]);
}


