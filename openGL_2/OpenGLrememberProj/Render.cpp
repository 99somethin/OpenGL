void create_circle_edge()
{
	
	const float radius = 5;
	const int segments = 50;

	glPushMatrix();
	glTranslated(6, 4, 0);
	glRotated(-37, 0, 0, 1);


	
	glNormal3f(0, 0, -1);
	glBegin(GL_TRIANGLE_FAN);
	glColor3d(0.3, 0.2, 0.5);
	glVertex3d(0, 0, 0); 
	for (int i = 0; i <= segments; i++)
	{
		float theta = 2.0f * 3.1415926f * float(i) / float(segments); 
		if (theta <= 3.4)
		{
			float x = radius * cosf(theta); 
			float y = radius * sinf(theta); 
			glVertex3d(x, y, 0); 
		}
	}
	glEnd();

	
	glNormal3f(0, 0, 1);
	glBegin(GL_TRIANGLE_FAN);
	glColor3d(0.3, 0.2, 0.5);
	glTexCoord2f(0.5, 0.5); 
	glVertex3d(0, 0, 1); 
	for (int i = 0; i <= segments; i++)
	{
		float theta = 2.0f * 3.1415926f * float(i) / float(segments); 
		if (theta <= 3.4)
		{
			float x = radius * cosf(theta); 
			float y = radius * sinf(theta); 
			glTexCoord2f((x + radius) / (2 * radius), (y + radius) / (2 * radius));
			glVertex3d(x, y, 1); 
		}
	}
	glEnd();

	glPopMatrix();
	
}



void create_circle_side()
{
	float radius = 5;
	int segments = 50;

	glPushMatrix();

	glTranslated(6, 4, 1);

	glRotated(-90, 1, 0, 0);

	glRotated(37, 0, 1, 0);

	glColor3d(0.9, 0.9, 0.5);

	glBegin(GL_TRIANGLE_STRIP);

	for (int i = 0; i <= segments; i++)
	{
		float theta = 2.0f * 3.1415926f * float(i) / float(segments); 

		if (theta <= 3.4)
		{
			float x = radius * cosf(theta); 
			float z = radius * sinf(theta);

			
			glNormal3f(x, 0, z);
			glVertex3f(x, 1, z);

			
			glNormal3f(x, 0, z); 
			glVertex3f(x, 0, z);
		}
	}
	glEnd();

	glPopMatrix();
}


void internal()
{
	
	float radius = 8.06;
	
	float segments = 800;

	glPushMatrix();

	glTranslated(-9, -10, 0);

	glNormal3f(0, 0, -1);

	glBegin(GL_TRIANGLE_FAN);

	glColor3d(0.3, 0.2, 0.5);

	glVertex3d(10, 8, 0); 

	for (int i = 0; i <= segments / 5; i++)
	{
		float theta = 2.0f * 3.1415926f * float(i) / float(segments); 


		float x = radius * cosf(theta); 
		float y = radius * sinf(theta); 

		if (x <= 8.003 && y <= 7.03f)
		{
			glVertex3d(x, y, 0); 
			
		}

	}

	glEnd();

	glPopMatrix();


	glBegin(GL_TRIANGLES);

	glNormal3f(0, 0, -1);

	glVertex3d(0, 0, 0);
	glVertex3d(1, -2, 0);
	glVertex3d(-5, -3, 0);

	glEnd();


	glPushMatrix();

	glTranslated(-9, -10, 0);

	glNormal3f(0, 0, 1);

	glBegin(GL_TRIANGLE_FAN);


	glColor3d(0.3, 0.2, 0.5);
	glTexCoord2f(0.5, 0.5); 
	glVertex3d(10, 8, 1); 


	for (int i = 0; i <= segments / 5; i++)
	{
		float theta = 2.0f * 3.1415926f * float(i) / float(segments); 

		float x = radius * cosf(theta);
		float y = radius * sinf(theta); 

		
		float s = 0.5 * (1 + cosf(theta)); 
		float t =  0.5 * (1 + sinf(theta));

		if (x <= 8.003 && y <= 7.03f)
		{
			glTexCoord2f(s, t); 
			glVertex3d(x, y, 1); 
		}
	}
	glEnd();


	glPopMatrix();


	glBegin(GL_TRIANGLES);

	glNormal3f(0, 0, 1);

	glTexCoord2d(0.45 * 2, 0.5 * 2);
	glVertex3d(0, 0, 1);
	glTexCoord2d(0.5 * 2, 0.40 * 2);
	glVertex3d(1, -2, 1);
	glTexCoord2d(0.2 * 2, 0.35 * 2);
	glVertex3d(-5, -3, 1);

	glEnd();

}


void internal_circle()
{
	float radius = 8.06;
	int segments = 1000;

	glPushMatrix();

	glTranslated(-9, -10, 1);
	
	glRotated(-90, 1, 0, 0);

	glRotated(-7, 0, 1, 0);

	glBegin(GL_TRIANGLE_STRIP);

	glColor3d(0.9, 0.9, 0.5);

	for (int i = 0; i <= segments / 6; i++)
	{
		float theta = 2.0f * 3.1415926f * float(i) / float(segments); 


		float x = radius * cosf(theta); 
		float z = radius * sinf(theta); 

		if (x >= 4.80)
		{
			
			glNormal3f(-x, 0, -z);
			glVertex3f(x, 1, z);
			
			glNormal3f(-x, 0, -z);
			glVertex3f(x, 0, z);
		}
	}
	glEnd();

	glPopMatrix();
}




void Render(OpenGL *ogl)
{



	glDisable(GL_TEXTURE_2D);
	glDisable(GL_LIGHTING);

	glEnable(GL_DEPTH_TEST);
	if (textureMode)
		glEnable(GL_TEXTURE_2D);

	if (lightMode)
		glEnable(GL_LIGHTING);





	//настройка материала
	GLfloat amb[] = { 0.2, 0.2, 0.1, 1. };
	GLfloat dif[] = { 0.4, 0.65, 0.5, 1. };
	GLfloat spec[] = { 0.9, 0.8, 0.3, 1. };
	GLfloat sh = 0.1f * 256;


	//фоновая
	glMaterialfv(GL_FRONT, GL_AMBIENT, amb);
	//дифузная
	glMaterialfv(GL_FRONT, GL_DIFFUSE, dif);
	//зеркальная
	glMaterialfv(GL_FRONT, GL_SPECULAR, spec); \
		//размер блика
		glMaterialf(GL_FRONT, GL_SHININESS, sh);

	//чтоб было красиво, без квадратиков (сглаживание освещения)
	glShadeModel(GL_SMOOTH);
	//===================================
	//Прогать тут  


	//Начало рисования квадратика станкина

	/*glEnable(GL_BLEND);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);*/

	glBindTexture(GL_TEXTURE_2D, texId);

	int high_edge = 1;
	int down_edge = 0;

	create_edges(down_edge);
	create_edges(high_edge);
	create_side_edges();

	create_circle_edge();
	create_circle_side();

	internal();
	internal_circle();



	//конец рисования квадратика станкина


   //Сообщение вверху экрана

	
	glMatrixMode(GL_PROJECTION);	//Делаем активной матрицу проекций. 
	                                //(всек матричные операции, будут ее видоизменять.)
	glPushMatrix();   //сохраняем текущую матрицу проецирования (которая описывает перспективную проекцию) в стек 				    
	glLoadIdentity();	  //Загружаем единичную матрицу
	glOrtho(0, ogl->getWidth(), 0, ogl->getHeight(), 0, 1);	 //врубаем режим ортогональной проекции

	glMatrixMode(GL_MODELVIEW);		//переключаемся на модел-вью матрицу
	glPushMatrix();			  //сохраняем текущую матрицу в стек (положение камеры, фактически)
	glLoadIdentity();		  //сбрасываем ее в дефолт

	glDisable(GL_LIGHTING);



	GuiTextRectangle rec;		   //классик моего авторства для удобной работы с рендером текста.
	rec.setSize(300, 150);
	rec.setPosition(10, ogl->getHeight() - 150 - 10);


	std::stringstream ss;
	ss << "T - вкл/выкл текстур" << std::endl;
	ss << "L - вкл/выкл освещение" << std::endl;
	ss << "F - Свет из камеры" << std::endl;
	ss << "G - двигать свет по горизонтали" << std::endl;
	ss << "G+ЛКМ двигать свет по вертекали" << std::endl;
	ss << "Коорд. света: (" << light.pos.X() << ", " << light.pos.Y() << ", " << light.pos.Z() << ")" << std::endl;
	ss << "Коорд. камеры: (" << camera.pos.X() << ", " << camera.pos.Y() << ", " << camera.pos.Z() << ")" << std::endl;
	ss << "Параметры камеры: R="  << camera.camDist << ", fi1=" << camera.fi1 << ", fi2=" << camera.fi2 << std::endl;
	
	rec.setText(ss.str().c_str());
	rec.Draw();

	glMatrixMode(GL_PROJECTION);	  //восстанавливаем матрицы проекции и модел-вью обратьно из стека.
	glPopMatrix();


	glMatrixMode(GL_MODELVIEW);
	glPopMatrix();

}